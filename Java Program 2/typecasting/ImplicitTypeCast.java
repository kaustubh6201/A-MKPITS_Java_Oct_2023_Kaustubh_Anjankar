package com.mkpits.typecasting;

public class ImplicitTypeCast {

	public static void main(String[] args) {
		
		// typecasting is process smaller range data type converted into larger range datatype
		// it called as widening 
		// widening run automatically 
		
		// destination_variable > source_variable
		
		byte num1=50;
		byte num2=45;
		
		int result=num1+num2; // total value assigned into larger datatype
		System.out.println(result);
		
		int var1=856;
		int var2=789;
		
		float result1=var1+var2;
		System.out.println(result1);
		
		
		
		

	}

}
