package com.mkpits.operators;

public class ArithmaticOperators {
	
	
	public static void main(String[] args) {
		
		// arithmetic operator are the operator which is responsible for doing arithmetic operation
		// +,-,*,/
		
		int numb1=50;
		int numb2=483;
		
		System.out.println(numb1+numb2); // addition
		System.out.println(numb1-numb2);// substraction
		System.out.println(numb1*numb2);// multiplication
		System.out.println(numb2/numb2);// division
	}

}
